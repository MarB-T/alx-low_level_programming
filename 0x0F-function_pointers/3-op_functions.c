#include <stdio.h>

/**
 * op_add - adds two integers
 * @a: the first number
 * @b: the second number
 * Return: (a+b)
 */

int op_add(int a, int b)
{
	return ((a + b));
}

/**
 * op_sub - difference of two integers
 * @a: the first number
 * @b: the second number
 * Return: (a-b)
 */

int op_sub(int a, int b)
{
	return ((a - b));
}

/**
 * op_mul - multiplies two integers
 * @a: the first number
 * @b: the second number
 * Return: (a*b)
 */

int op_mul(int a, int b)
{
	return ((a * b));
}

/**
 * op_div - divides two integers
 * @a: the first number
 * @b: the second number
 * Return: (a/b)
 */

int op_div(int a, int b)
{
	return ((a / b));
}

/**
 * op_mod - modulo divide two integers
 * @a: the first number
 * @b: the second number
 * Return: (a%b)
 */

int op_mod(int a, int b)
{
	return ((a % b));
}
